export function buildTranslationMap(targetLocale: Locale) {
  const map = new Map<string, string>();
  for (const [left, right] of translationPairs) {
    if (left === right) continue;

    const leftIsRussian = /[А-Яа-яЁё]/.test(left);
    const rightIsRussian = /[А-Яа-яЁё]/.test(right);
    if (leftIsRussian === rightIsRussian) continue;

    const ru = leftIsRussian ? left : right;
    const en = leftIsRussian ? right : left;
    map.set(targetLocale === "ru" ? en : ru, targetLocale === "ru" ? ru : en);
  }
  return map;
}
